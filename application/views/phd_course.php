                <p><b>1.1. Institute Stipendiary Full Time Research Scholar (Course Code-11):</b></p>
                <p>These scholars shall be eligible for financial assistance in the form of fellowship at the approved rates and guide lines as
notified by MHRD from time to time. The fellowship shall be awarded to the students of this category on a Semester–to-
Semester basis depending on the satisfactory academic performance of the scholar</p>
                <p><b>1.2 Self-financed Full Time Research Scholar (Course Code-12):</b></p>
                <p>Self-financed full-time research scholars are those who support themselves or receive fellowship from other agencies such
as UGC, CSIR, and DST etc. They shall not receive any financial support from the Institute in any form</p>
                
                <p><b>1.3 Sponsored Full-Time Research Scholar(Course Code-13):</b></p>
                <p>(a) Sponsored Full-Time Research Scholars are those who receive financial support from AICTE under QIP scheme
OR from Government/Semi-Government Institutions/Government/Semi-Government Organizations under study
leave. Such a candidate must be sponsored by his/her employer and must have been in employment with the
sponsoring organization for at least two years at the time of admission. Candidates in service are to be released
on study leave or extraordinary leave from the organization for full-time research work at the Institute and must
furnish a sponsorship along with No-objection certificate from the employer.</p>
                <p>(b) Foreign students may be sponsored by their Governments and awarded scholarship by Govt. of India, Indian
Council for Culture Relations (ICCR) and other such agencies/organisations</p>
                <p><b>1.4. Faculty/Staff/Sponsored Project Fellow of MNNIT Allahabad Part Time Research Scholars (Course Code - 14):</b></p>
                <p>Candidates under this category shall be Faculty/Staff of the Institute/Project Fellow working in the various sponsored
research projects undertaken by the Institute. Project Fellow, if admitted, shall be required to pursue his/her Ph. D. research
work in the area of research project and with PI and/or Co-PI only.</p>
                <p><b>2. Eligibility for Admission</b></p>
                <p>(i) Candidates must have minimum marks of 60% aggregate or 6.5 C.P.I. (Cumulative Performance Index in ten point
scale) in qualifying examination. For SC/ST candidate’s eligibility in specified minimum marks will be relaxed by 5% as
per the Government of India norms.</p>
                <p>(ii) The eligibility conditions given above are the absolute minimum. Departments may prescribe any requirements over
and above these, subject to the approval of the Chairman, Senate.</p>
                <p><b>2.1. Ph.D. in Engineering</b></p>
                <p>The applicant with a Master degree in relevant branch of Engineering/Technology with marks not below 60% or CPI 6.5</p>
                <p class="text-center">OR</p>
                <p>Applicant with a Bachelors degree in Engineering or Masters Degree in Science/Applied biological sciences/Computer
Application or any appropriate discipline with a minimum of 75% marks or CPI 8.0 shall only be considered</p>
